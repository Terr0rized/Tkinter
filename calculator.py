
firs_number = int(input("Enter number: "))
second_number = int(input('Enter number: '))
operation = input("Enter operation: ")
if operation == '+':
    print(firs_number + second_number)
elif operation == '-':
    print(firs_number - second_number)
elif operation == '/':
    print(firs_number // second_number)
elif operation == '*':
    print(firs_number * second_number)
